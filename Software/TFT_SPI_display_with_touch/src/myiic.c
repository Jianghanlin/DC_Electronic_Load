#include "myiic.h"
#include "delay.h"

/*****************************************************************************
 * @name       :void IIC_Init(void)
 * @date       :2018-08-09 
 * @function   :Initialize IIC
 * @parameters :None
 * @retvalue   :None
 ******************************************************************************/
void IIC_Init(void) //片外AT24C02初始化函数
{
    P6DIR |= BIT6 + BIT7; //P6.6是SCL，P6.7是SDA

    IIC_SCL_H;
    IIC_SDA_H;
}

/*****************************************************************************
 * @name       :void IIC_Start(void)
 * @date       :2018-08-09 
 * @function   :Generating IIC starting signal
 * @parameters :None
 * @retvalue   :None
 ******************************************************************************/
void IIC_Start(void)
{
    SDA_OUT();     //sda线输出
    IIC_SDA_H;
    IIC_SCL_H;
    delay_us(4);
    IIC_SDA_L;     //START:when CLK is high,DATA change form high to low
    delay_us(4);
    IIC_SCL_L;     //钳住I2C总线，准备发送或接收数据
}

/*****************************************************************************
 * @name       :void IIC_Stop(void)
 * @date       :2018-08-09 
 * @function   :Generating IIC stop signal
 * @parameters :None
 * @retvalue   :None
 ******************************************************************************/
void IIC_Stop(void)
{
    SDA_OUT();     //sda线输出
    IIC_SCL_L;
    IIC_SDA_L;     //STOP:when CLK is high DATA change form low to high
    delay_us(4);
    IIC_SCL_H;
    IIC_SDA_H;     //发送I2C总线结束信号
    delay_us(4);
}

/*****************************************************************************
 * @name       :uint8_t IIC_Wait_Ack(void)
 * @date       :2018-08-09 
 * @function   :Wait for the response signal
 * @parameters :None
 * @retvalue   :0-receive response signal successfully
 1-receive response signal unsuccessfully
 ******************************************************************************/
uint8_t IIC_Wait_Ack(void)
{
    uint8_t ucErrTime = 0;
    SDA_IN();      //SDA设置为输入
    IIC_SDA_H;
    delay_us(1);
    IIC_SCL_H;
    delay_us(1);
    while (READ_SDA)
    {
        ucErrTime++;
        if (ucErrTime > 250)
        {
            IIC_Stop();
            return 1;
        }
    }
    IIC_SCL_L;      //时钟输出0
    return 0;
}

/*****************************************************************************
 * @name       :void IIC_Ack(void)
 * @date       :2018-08-09 
 * @function   :Generate ACK response signal
 * @parameters :None
 * @retvalue   :None
 ******************************************************************************/
void IIC_Ack(void)
{
    IIC_SCL_L;
    SDA_OUT();
    IIC_SDA_L;
    delay_us(2);
    IIC_SCL_H;
    delay_us(2);
    IIC_SCL_L;
}

/*****************************************************************************
 * @name       :void IIC_NAck(void)
 * @date       :2018-08-09 
 * @function   :Don't generate ACK response signal
 * @parameters :None
 * @retvalue   :None
 ******************************************************************************/
void IIC_NAck(void)
{
    IIC_SCL_L;
    SDA_OUT();
    IIC_SDA_H;
    delay_us(2);
    IIC_SCL_H;
    delay_us(2);
    IIC_SCL_L;
}

/*****************************************************************************
 * @name       :void IIC_Send_Byte(uint8_t txd)
 * @date       :2018-08-09 
 * @function   :send a byte data by IIC bus
 * @parameters :txd:Data to be sent
 * @retvalue   :None
 ******************************************************************************/
void IIC_Send_Byte(uint8_t txd)
{
    uint8_t t;
    SDA_OUT();
    IIC_SCL_L;      //拉低时钟开始数据传输
    for (t = 0; t < 8; t++)
    {
        if ((txd << t) & 0x80) //从最高位开始写
        {
            IIC_SDA_H;
        }
        else
        {
            IIC_SDA_L;
        }
        delay_us(2);   //对TEA5767这三个延时都是必须的
        IIC_SCL_H;
        delay_us(2);
        IIC_SCL_L;
        delay_us(2);
    }
}

/*****************************************************************************
 * @name       :uint8_t IIC_Read_Byte(unsigned char ack)
 * @date       :2018-08-09 
 * @function   :read a byte data by IIC bus
 * @parameters :ack:0-send nACK
 1-send ACK
 * @retvalue   :Data to be read
 ******************************************************************************/
uint8_t IIC_Read_Byte(unsigned char ack)
{
    unsigned char i, receive = 0;
    SDA_IN();   //SDA设置为输入
    for (i = 0; i < 8; i++)
    {
        IIC_SCL_L;
        delay_us(2);
        IIC_SCL_H;
        receive <<= 1;
        if (READ_SDA)
            receive++;
        delay_us(1);
    }
    if (!ack)
        IIC_NAck();   //发送nACK
    else
        IIC_Ack(); //发送ACK   
    return receive;
}

